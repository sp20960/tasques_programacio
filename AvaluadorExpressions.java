public class AvaluadorExpressions {
    public static void main(String[] args) {
        int a = 5;
        int b = 9;
        int c = 3;
        int d = 4;

        System.out.println(2 - a * b + c);
        System.out.println((2 - a) * b +c);
        System.out.println(a * b - c * a -d);
        System.out.println(a / 3 - b);
        System.out.println(d * 23 -1 + b);
    }
}
