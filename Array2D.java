public class Array2D {
    public static void main(String[] args) {
        int [][] array = {
                         {1,2,3},
                         {5,8,9}
        };
        System.out.println((array[0][0] + array[0][1] + array[0][2])/3);
        System.out.println((array[1][0] + array[1][1] + array[1][2])/3);
    }
}
